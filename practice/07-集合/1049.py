# 题目描述
# 利用集合分析活动投票情况。第一小队有五名队员，序号是1,2,3,4,5;
# 第二小队也有五名队员，序号6,7,8,9,10。
# 输入一个得票字符串，得票的队员的序列号用逗号隔开，分别输出两个小队没有得票的队员序号。
# 每个小队一行，序号按从小到大顺序输出，队员序号之间用空格分隔。
# 读取得票字符串并转换为整数集合
vote_set = set(map(int, input().strip().split(',')))

# 定义小队并找出没有得票的队员
team1 = sorted({1, 2, 3, 4, 5} - vote_set)
team2 = sorted({6, 7, 8, 9, 10} - vote_set)

# 输出结果
print(' '.join(map(str, team1)))
print(' '.join(map(str, team2)))